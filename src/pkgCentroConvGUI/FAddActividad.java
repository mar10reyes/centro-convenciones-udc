    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        String tipo = "";
        
        String nombre = txtNombre.getText();
        String actividad = txtActividad.getText();
        
        //escoger el tipo de actividad
        switch(cmbTipo.getSelectedIndex())
        {
            case 0:
            {
                tipo = "Academica";
                break;
            }
            case 1:
            {
                tipo = "culturales";
                break;
            }
        }
        
        int precio = Integer.parseInt(txtPrecio.getText());
        Actividad act = new Actividad(nombre, tipo, precio, actividad);
        
        int n = CcApp.cc.buscarEvento(txtActividad.getText());
        
        //si no se encontro
        if(n == -1)
        {
            JOptionPane.showMessageDialog(null, "No se encontro el evento");
        }
        else
        {
            if (CcApp.cc.getEvento(n).buscarActividad(nombre) == -1)
            {
                CcApp.cc.getEvento(n).addActividad(act);

                JOptionPane.showMessageDialog(null, "Operaci√≥n realizada correctamente");

                FAdministrarEvento fAdministrarEvento = new FAdministrarEvento();
                fAdministrarEvento.setVisible(true);
                this.setVisible(false);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "La actividad ya existe");
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

