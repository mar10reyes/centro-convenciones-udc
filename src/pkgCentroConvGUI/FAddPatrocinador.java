    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        //se valida que no esten vacios los campos
        if(txtNombre.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Hay campos vacios");
        }
        else
        {
            Patrocinador p = new Patrocinador(txtNombre.getText(), txtTipo.getText());
            
            int n = CcApp.cc.buscarEvento(txtEvento.getText());
            
            //verificar si existe el evento
            if(n == -1)
            {
                JOptionPane.showMessageDialog(null, "no se encontro el evento");
            }
            else
            {
                if(CcApp.cc.getEvento(n).buscarPatrocinador(txtNombre.getText()) != -1)
                {
                    JOptionPane.showMessageDialog(null, "El patrocinador ya existe");
                }
                else
                {
                    //se agrega el patrocinador solo si no esta repetido
                    CcApp.cc.getEvento(n).addPatrocinador(p);
                    JOptionPane.showMessageDialog(null, "Se ha a√±adido un patrocinador");
                }
                
            }
        }
        
        FAdministrarEvento fAdministrarEvento = new FAdministrarEvento();
        fAdministrarEvento.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_btnGuardarActionPerformed

